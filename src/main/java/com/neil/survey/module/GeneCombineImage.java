package com.neil.survey.module;


public class GeneCombineImage {
	    private String master;
	    private String driverRoom;
	    private String wheel;
	    private String rearHood;
	    private String mode;
	    public void setMaster(String master) {
	         this.master = master;
	     }
	     public String getMaster() {
	         return master;
	     }

	    public void setDriverRoom(String driverRoom) {
	         this.driverRoom = driverRoom;
	     }
	     public String getDriverRoom() {
	         return driverRoom;
	     }

	    public void setWheel(String wheel) {
	         this.wheel = wheel;
	     }
	     public String getWheel() {
	         return wheel;
	     }
	    public void setRearHood(String rearHood) {
	         this.rearHood = rearHood;
	     }
	     public String getRearHood() {
	         return rearHood;
	     }
		public String getMode() {
			return mode;
		}
		public void setMode(String mode) {
			this.mode = mode;
		}
}
